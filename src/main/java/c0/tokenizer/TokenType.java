package c0.tokenizer;

public enum TokenType {
    /** 空 */
    None,
    /**fn*/
    ty,
    /**类型*/
    FN_KW,
    /**let*/
    LET_KW,
    /**const*/
    CONST_KW,
    /**as*/
    AS_KW,
    /**while*/
    WHILE_KW,
    /**if*/
    IF_KW,
    /**else*/
    ELSE_KW,
    /**return*/
    RETURN_KW,
    /**break*/
    BREAK_KW,
    /**continue*/
    CONTINUE_KW,
    /**无符号整数*/
    UINT_LITERAL,
    /**字符串*/
    STRING_LITERAL,
    /**浮点数*/
    DOUBLE_LITERAL,
    /**字符*/
    CHAR_LITERAL,
    /**标识符*/
    IDENT,
    /**加号*/
    PLUS,
    /**减号*/
    MINUS,
    /**乘号*/
    MUL,
    /**除号*/
    DIV,
    /**=*/
    ASSIGN,
    /**==*/
    EQ,
    /**!=*/
    NEQ,
    /**<*/
    LT,
    /**>*/
    GT,
    /**<=*/
    LE,
    /**>=*/
    GE,
    /**(*/
    L_PAREN,
    /**)*/
    R_PAREN,
    /**{*/
    L_BRACE,
    /**}*/
    R_BRACE,
    /**->*/
    ARROW,
    /**,*/
    COMMA,
    /**:*/
    COLON,
    /**;*/
    SEMICOLON,
    /**注释*/
    COMMENT,
    /**文件尾*/
    EOF;

}
